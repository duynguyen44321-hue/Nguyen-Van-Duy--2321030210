# De bai:
# Viet chuong trinh nhap vao 2 so nguyen duong m va n,
# sau do tinh tong (m + n)
# va tim ra chu so lon nhat trong tong do.

# Nhập 2 số nguyên dương
m = int(input("Nhap m: "))
n = int(input("Nhap n: "))

tong = m + n

print("Tong =", tong)

chu_so_lon_nhat = max(str(tong))

print("Chu so lon nhat trong tong =", chu_so_lon_nhat)